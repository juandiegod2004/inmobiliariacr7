create table if not exists public.visits (
  id bigint generated by default as identity primary key,
  property_id bigint references public.properties(id) on delete cascade,
  name text not null,
  phone text not null,
  visit_date date not null,
  message text,
  created_at timestamptz default now()
);

-- Habilitar RLS en visits
alter table public.visits enable row level security;

-- Cualquiera puede insertar (agendar) una visita
create policy "Cualquiera agenda visitas"
  on public.visits for insert
  with check (true);

-- Solo ADMIN y AGENT pueden ver visitas
create policy "ADMIN y AGENT ven visitas"
  on public.visits for select
  using (public.is_agent_or_admin());

-- Solo ADMIN puede eliminar registros de visitas
create policy "Solo ADMIN elimina visitas"
  on public.visits for delete
  using (public.is_admin());


